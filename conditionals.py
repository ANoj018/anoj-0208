# 1. If.....Else statement

age = int(input("Enter your age: "))

if age == 18:
    print("you are okay to drive")
elif age < 30:
    print("you are not okay to drive")
elif age > 30:
    print("you are okay to drive")
else:
    print("you are okay to drive but you are not 18 yet")

